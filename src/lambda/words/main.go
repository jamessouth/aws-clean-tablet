func handler() ([]string, error) {
	return shuffleList(words), nil
}

func main() {
	lambda.Start(handler)
}

func shuffleList(l []string) []string {
	t := time.Now().UnixNano()
	rand.Seed(t)

	nl := append([]string(nil), l...)

	rand.Shuffle(len(nl), func(i, j int) {
		nl[i], nl[j] = nl[j], nl[i]
	})

	return nl[:40]
}

